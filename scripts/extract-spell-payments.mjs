/**
 * xlsx → data/spell-payments-to-prime-subproxies.md (parsed on into
 * src/lib/spell-data.ts by scripts/generate-data.mjs).
 *
 * Usage:
 *   node scripts/extract-spell-payments.mjs <workbook.xlsx>
 *   SPELL_PAYMENTS_XLSX=<path> node scripts/extract-spell-payments.mjs
 */
import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath } from "node:url";
import * as XLSX from "xlsx";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const OUT = path.join(ROOT, "data", "spell-payments-to-prime-subproxies.md");

const IN_COLUMNS = [
  "Cast date",
  "Prime",
  "USDS",
  "Kind",
  "Settles accrual",
  "Receiving wallet (subproxy)",
  "Tx hash",
  "Spell",
  "Spell address",
  "Subproxy constant",
  "Section",
];
const OUT_HEADER = IN_COLUMNS.map((c) => (c === "Section" ? "Label" : c));

const str = (v) => (v === null || v === undefined ? "" : String(v).trim());

function usds(v) {
  const t = str(v).replaceAll("$", "").replaceAll(",", "");
  if (t === "") return "0";
  const n = Number(t);
  if (Number.isNaN(n)) throw new Error(`USDS is not a number: "${str(v)}"`);
  return String(Math.round(n));
}

function labelFor(kind, section) {
  if (kind === "settlement cycle") return "MSC";
  const s = section.toLowerCase();
  if (/genesis|funding|launch/.test(s)) return "Genesis Transfer";
  if (/transfer/.test(s)) return "Transfer";
  throw new Error(
    `Cannot derive Label for row (kind="${kind}", section="${section}") — ` +
      "extend labelFor() in scripts/extract-spell-payments.mjs",
  );
}

function main() {
  const xlsxPath = process.argv[2] || process.env.SPELL_PAYMENTS_XLSX;
  if (!xlsxPath) {
    throw new Error(
      "No workbook given. Pass the .xlsx path as an argument or set SPELL_PAYMENTS_XLSX.",
    );
  }
  if (!fs.existsSync(xlsxPath)) throw new Error(`Workbook not found: ${xlsxPath}`);

  const wb = XLSX.read(fs.readFileSync(xlsxPath));
  const sheet = wb.Sheets["Spell payments"] ?? wb.Sheets[wb.SheetNames[0]];
  const grid = XLSX.utils.sheet_to_json(sheet, { header: 1, raw: true, defval: null });
  if (!grid.length) throw new Error("workbook sheet is empty");

  const header = grid[0].map(str);
  const idx = IN_COLUMNS.map((c) => header.indexOf(c));
  const missing = IN_COLUMNS.filter((_, i) => idx[i] === -1);
  if (missing.length) {
    throw new Error(`workbook header changed — columns not found: ${missing.join(", ")}`);
  }

  const dataRows = grid.slice(1).filter((r) => str(r[idx[0]]) || str(r[idx[1]]));
  const mdRows = dataRows.map((r) => {
    const kind = str(r[idx[3]]);
    const section = str(r[idx[10]]);
    const cells = IN_COLUMNS.map((col, i) => {
      if (col === "USDS") return usds(r[idx[i]]);
      if (col === "Section") return labelFor(kind, section);
      return str(r[idx[i]]);
    });
    return `| ${cells.join(" | ")} |`;
  });

  const md = `# Spell payments to prime subproxies

Source-of-truth data for the Spell Payments view. One row per on-chain spell
payment to a prime subproxy.

GENERATED by scripts/extract-spell-payments.mjs from the
"DR spell payments to prime subproxies.xlsx" workbook — do not edit by hand;
update the workbook and re-run \`node scripts/extract-spell-payments.mjs <path>\`.
This file is in turn parsed into src/lib/spell-data.ts by scripts/generate-data.mjs.

- **USDS** — whole USDS transferred (not wei).
- **Kind** — \`settlement cycle\` or \`other\` (genesis / capital transfers).
- **Settles accrual** — accrual month(s) the settlement cycle covers; blank for \`other\`.
- **Label** — \`MSC\`, \`Genesis Transfer\`, or \`Transfer\`.

| ${OUT_HEADER.join(" | ")} |
| ${OUT_HEADER.map(() => "---").join(" | ")} |
${mdRows.join("\n")}
`;

  fs.writeFileSync(OUT, md);
  console.log(`[extract-spell-payments] wrote ${mdRows.length} rows → ${path.relative(ROOT, OUT)}`);
}

main();
