/**
 * Dune → data/spell-payments-to-prime-subproxies.md → src/lib/spell-data.ts.
 * Fetches the latest results of the Dune query and updates the markdown file.
 *
 * Usage:
 *   DUNE_API_KEY=... DUNE_QUERY_ID=1234567 node scripts/fetch-spell-payments.mjs
 *   DUNE_API_KEY=... node scripts/fetch-spell-payments.mjs 1234567
 */
import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const MD = path.join(ROOT, "data", "spell-payments-to-prime-subproxies.md");
const TS = path.join(ROOT, "src", "lib", "spell-data.ts");

// data/*.md column order. ONCHAIN come from Dune; EDITORIAL are preserved.
const HEADER = [
  "Cast date",
  "Prime",
  "USDS",
  "Kind",
  "Settles accrual",
  "Receiving wallet (subproxy)",
  "Tx hash",
  "Spell",
  "Spell address",
  "Subproxy constant",
  "Label",
];
const EDITORIAL = new Set(["Kind", "Settles accrual", "Spell", "Subproxy constant", "Label"]);

const str = (v) => (v === null || v === undefined ? "" : String(v).trim());
const key = (txHash, wallet) => `${str(txHash).toLowerCase()}|${str(wallet).toLowerCase()}`;

function usdsStr(v) {
  const n = Number(str(v).replaceAll(",", ""));
  return Number.isNaN(n) ? str(v) : String(n);
}

function parseMdRows(md) {
  const rows = [];
  for (const line of md.split("\n")) {
    if (!line.trim().startsWith("|")) continue;
    const cells = line.trim().slice(1, -1).split("|").map(str);
    if (cells.every((c) => /^:?-+:?$/.test(c))) continue;
    if (cells[0] === HEADER[0]) continue; // header row
    if (cells.length !== HEADER.length) continue;
    rows.push(Object.fromEntries(HEADER.map((h, i) => [h, cells[i]])));
  }
  return rows;
}

function preamble() {
  return `# Spell payments to prime subproxies

Source of truth for the Spell Payments view. One row per on-chain spell payment
to a prime subproxy. Parsed into src/lib/spell-data.ts by scripts/generate-data.mjs.

On-chain columns (cast date, USDS, tx hash, spell address, receiving wallet) come
from the Dune query in scripts/dune/spell-payments-to-prime-subproxies.sql
(https://dune.com/queries/8040299). The editorial columns below are added by hand
and preserved on refresh (scripts/fetch-spell-payments.mjs), which keeps existing
rows verbatim and only appends payments the query newly returns:

- **USDS** — whole USDS transferred (not wei).
- **Kind** — \`settlement cycle\` or \`other\` (genesis / capital transfers).
- **Settles accrual** — accrual month(s) the settlement cycle covers; blank for \`other\`.
- **Label** — \`MSC\`, \`Genesis Transfer\`, or \`Transfer\`.`;
}

function writeMd(rows) {
  const body = rows
    .map((r) => `| ${HEADER.map((h) => r[h] ?? "").join(" | ")} |`)
    .join("\n");
  const md = `${preamble()}

| ${HEADER.join(" | ")} |
| ${HEADER.map(() => "---").join(" | ")} |
${body}
`;
  fs.writeFileSync(MD, md);
}

// mirrors the spell step of scripts/generate-data.mjs
const TS_COLUMNS = [
  ["Cast date", "castDate", str],
  ["Prime", "prime", str],
  ["USDS", "usds", (v) => Number(str(v).replaceAll(",", "")) || 0],
  ["Kind", "kind", str],
  ["Settles accrual", "settlesAccrual", str],
  ["Receiving wallet (subproxy)", "receivingWallet", str],
  ["Tx hash", "txHash", str],
  ["Spell", "spell", str],
  ["Spell address", "spellAddress", str],
  ["Subproxy constant", "subproxyConstant", str],
  ["Label", "label", str],
];

function writeTs(rows) {
  const payments = rows.map((r) =>
    Object.fromEntries(TS_COLUMNS.map(([h, k, parse]) => [k, parse(r[h])])),
  );
  const ts = `/**
 * Sky Spell Payments to prime subproxies — dataset.
 *
 * GENERATED by scripts/generate-data.mjs from
 * data/spell-payments-to-prime-subproxies.md.
 * Do not edit by hand — run \`pnpm generate-data\` instead.
 *
 * One row per on-chain spell payment. USDS is whole USDS (not wei).
 */
import type { SpellPayment } from "./spell-types";

export const spellPayments: SpellPayment[] = ${JSON.stringify(payments, null, 2)};
`;
  fs.writeFileSync(TS, ts);
}

async function fetchDune(queryId, apiKey) {
  const url = `https://api.dune.com/api/v1/query/${queryId}/results?limit=10000`;
  const res = await fetch(url, { headers: { "X-Dune-API-Key": apiKey } });
  if (!res.ok) {
    throw new Error(`Dune API ${res.status}: ${await res.text()}`);
  }
  const data = await res.json();
  const rows = data?.result?.rows;
  if (!rows) throw new Error("Dune response has no result.rows — run the query on Dune at least once");
  return rows;
}

async function main() {
  const queryId = process.argv[2] || process.env.DUNE_QUERY_ID;
  const apiKey = process.env.DUNE_API_KEY;
  if (!queryId) throw new Error("No query id. Pass it as an argument or set DUNE_QUERY_ID.");
  if (!apiKey) throw new Error("No DUNE_API_KEY in the environment.");

  const duneRows = await fetchDune(queryId, apiKey);
  const existing = new Map(
    parseMdRows(fs.readFileSync(MD, "utf8")).map((r) => [
      key(r["Tx hash"], r["Receiving wallet (subproxy)"]),
      r,
    ]),
  );

  // Existing rows are kept verbatim (preserves editorial + address casing); only
  // payments the query newly returns are appended, with blank editorial fields.
  const seen = new Set();
  const merged = [];
  const needClassifying = [];
  for (const d of duneRows) {
    const k = key(d.tx_hash, d.receiving_wallet);
    seen.add(k);
    const prev = existing.get(k);
    if (prev) {
      merged.push(prev);
      continue;
    }
    const row = {
      "Cast date": str(d.cast_date).slice(0, 10),
      Prime: str(d.prime),
      USDS: usdsStr(d.usds),
      "Receiving wallet (subproxy)": str(d.receiving_wallet),
      "Tx hash": str(d.tx_hash),
      "Spell address": str(d.spell_address),
    };
    for (const h of HEADER) {
      if (EDITORIAL.has(h)) row[h] = h === "Subproxy constant" ? str(d.subproxy_constant) : "";
    }
    needClassifying.push(`${row["Cast date"]} ${row.Prime} ${row.USDS} (${row["Tx hash"]})`);
    merged.push(row);
  }

  // keep any hand rows the query didn't return, rather than silently dropping them
  const orphans = [...existing.values()].filter(
    (r) => !seen.has(key(r["Tx hash"], r["Receiving wallet (subproxy)"])),
  );
  for (const o of orphans) merged.push(o);

  merged.sort((a, b) => {
    if (a["Cast date"] !== b["Cast date"]) return a["Cast date"] < b["Cast date"] ? 1 : -1;
    return (Number(b.USDS) || 0) - (Number(a.USDS) || 0);
  });

  writeMd(merged);
  writeTs(merged);

  console.log(`[fetch-spell-payments] ${merged.length} rows → ${path.relative(ROOT, MD)} + ${path.relative(ROOT, TS)}`);
  if (orphans.length) {
    console.warn(`[fetch-spell-payments] ${orphans.length} row(s) in the markdown were NOT returned by Dune (kept):`);
    for (const o of orphans) console.warn(`  - ${o["Cast date"]} ${o.Prime} (${o["Tx hash"]})`);
  }
  if (needClassifying.length) {
    console.warn(`[fetch-spell-payments] ${needClassifying.length} new row(s) need Kind / Settles accrual / Label filled in by hand:`);
    for (const n of needClassifying) console.warn(`  - ${n}`);
  }
}

main().catch((e) => {
  console.error(`[fetch-spell-payments] ${e.message}`);
  process.exit(1);
});
